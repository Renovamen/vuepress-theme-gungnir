export interface MdPlusOptions {
  /**
   * Enable all functions or not.
   *
   * @default false
   */
  all?: boolean;

  /**
   * Enable "footnote" or not.
   *
   * @default false
   */
  footnote?: boolean;

  /**
   * Enable "mark" or not.
   *
   * @default false
   */
  mark?: boolean;
  /**
   * Enable Superscript or not.
   *
   * @default false
   */
  sup?: boolean;
  /**
   * Enable Subscript or not.
   *
   * @default false
   */
  sub?: boolean;
}
